 * ARCHITECTURE OVERVIEW
 * =====================
 * This plugin stores comparison data in a Custom Post Type (comparison_topic) with
 * a custom taxonomy (comparison_category) for grouping topics into accordion panels.
 * Each post holds a repeatable 'churches' meta field: an array of {church_name, description}.
 *
 * Data model:
 *   CPT:  comparison_topic        — one post per sub-topic (e.g. "Baptism", "Holy Scripture")
 *   Tax:  comparison_category     — groups topics (e.g. "Sacraments", "God", "Eschatology")
 *   Meta: churches (post)         — [{church_name: string, description: string (HTML)}, ...]
 *   Meta: sort_order (post)       — integer, display order within a category
 *   Meta: sort_order (term)       — integer, display order of categories in the accordion
 *
 * The single Gutenberg block (comparison-accordion) renders server-side:
 *   Desktop → CSS Grid table with aligned paragraphs across churches
 *   Mobile  → stacked layout: topic heading, then each church label + its paragraphs
 *
 * Plugin structure:
 *   app/          — PHP classes (PSR-4 autoloaded, namespace ComparisonOfReligions)
 *   src/blocks/   — Gutenberg block source (JS, SCSS, render.php)
 *   build/        — Compiled block assets (generated by `npm run build`)
 *   tools/        — One-time data import/export scripts
 */

