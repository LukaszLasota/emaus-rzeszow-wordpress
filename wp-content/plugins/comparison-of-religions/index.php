/**
 * Render the admin import/export page.
 *
 * Provides three operations:
 *   1. Import from JSON file upload (categories + topics with church descriptions)
 *   2. Export current data as downloadable JSON file
 *   3. Run built-in hardcoded data import (one-time seeding)
 */
function cor_import_page(): void {
	if ( ! current_user_can( 'manage_options' ) ) {
		wp_die( esc_html__( 'Brak uprawnień.', 'comparison-of-religions' ) );
	}

	require_once COR_PLUGIN_DIR . 'tools/import-html-data.php';

	// Handle JSON export — streams a .json file download to the browser.
	if ( isset( $_POST['cor_export_json'] ) && check_admin_referer( 'cor_import_nonce' ) ) {
		$data = cor_export_comparison_data();
		$json = wp_json_encode( $data, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE );

		header( 'Content-Type: application/json; charset=utf-8' );
		header( 'Content-Disposition: attachment; filename="comparison-of-religions-export.json"' );
		header( 'Content-Length: ' . strlen( $json ) );
		echo $json; // phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- JSON file download.
		exit;
	}

	// Handle built-in hardcoded data import (seeds the database with default comparison data).
	if ( isset( $_POST['cor_run_import'] ) && check_admin_referer( 'cor_import_nonce' ) ) {
		echo '<div class="wrap"><h1>' . esc_html__( 'Import danych', 'comparison-of-religions' ) . '</h1><pre>';
		cor_run_import();
		echo '</pre><p><strong>' . esc_html__( 'Import zakończony.', 'comparison-of-religions' ) . '</strong></p></div>';
		return;
	}

	// Handle JSON file upload — reads uploaded .json and imports categories + topics.
	if ( isset( $_POST['cor_json_import'] ) && check_admin_referer( 'cor_import_nonce' ) ) {
		echo '<div class="wrap"><h1>' . esc_html__( 'Import z JSON', 'comparison-of-religions' ) . '</h1><pre>';

		if ( empty( $_FILES['cor_json_file']['tmp_name'] ) ) {
			echo esc_html__( 'ERROR: Nie wybrano pliku.', 'comparison-of-religions' ) . "\n";
		} else {
			$tmp_file = sanitize_text_field( $_FILES['cor_json_file']['tmp_name'] );
			// phpcs:ignore WordPress.WP.AlternativeFunctions.file_get_contents_file_get_contents -- Reading uploaded temp file.
			$raw_json = file_get_contents( $tmp_file );
			$data     = json_decode( $raw_json, true );

			if ( null === $data || ! is_array( $data ) ) {
				echo esc_html__( 'ERROR: Nieprawidłowy format JSON.', 'comparison-of-religions' ) . "\n";
			} else {
				cor_run_json_import( $data );
			}
		}

		echo '</pre><p><strong>' . esc_html__( 'Import zakończony.', 'comparison-of-religions' ) . '</strong></p></div>';
		return;
	}

	?>
	<div class="wrap">
		<h1><?php esc_html_e( 'Import / Eksport — Porównanie wyznań', 'comparison-of-religions' ); ?></h1>

		<h2><?php esc_html_e( 'Import z pliku JSON', 'comparison-of-religions' ); ?></h2>
		<p><?php esc_html_e( 'Wgraj plik .json z danymi porównania (kategorie + tematy z opisami kościołów).', 'comparison-of-religions' ); ?></p>
		<form method="post" enctype="multipart/form-data">
			<?php wp_nonce_field( 'cor_import_nonce' ); ?>
			<p>
				<input type="file" name="cor_json_file" accept=".json,application/json" />
			</p>
			<p>
				<button type="submit" name="cor_json_import" value="1" class="button button-primary">
					<?php esc_html_e( 'Importuj z JSON', 'comparison-of-religions' ); ?>
				</button>
			</p>
		</form>

		<hr />

		<h2><?php esc_html_e( 'Eksport do JSON', 'comparison-of-religions' ); ?></h2>
		<p><?php esc_html_e( 'Pobierz aktualne dane jako plik JSON (do przeniesienia na inną instancję WP).', 'comparison-of-religions' ); ?></p>
		<form method="post">
			<?php wp_nonce_field( 'cor_import_nonce' ); ?>
			<p>
				<button type="submit" name="cor_export_json" value="1" class="button button-secondary">
					<?php esc_html_e( 'Pobierz JSON', 'comparison-of-religions' ); ?>
				</button>
			</p>
		</form>

		<hr />

		<h2><?php esc_html_e( 'Import wbudowanych danych', 'comparison-of-religions' ); ?></h2>
		<p><?php esc_html_e( 'Jednorazowy import zakodowanych na stałe danych (Kościół Rzymskokatolicki vs Zielonoświątkowy).', 'comparison-of-religions' ); ?></p>
		<form method="post">
			<?php wp_nonce_field( 'cor_import_nonce' ); ?>
			<p>
				<button type="submit" name="cor_run_import" value="1" class="button button-secondary">
					<?php esc_html_e( 'Importuj dane wbudowane', 'comparison-of-religions' ); ?>
				</button>
			</p>
		</form>
	</div>
	<?php
}

/**
 * Invalidate the comparison-accordion block's transient cache.
 *
 * Called automatically when any comparison_topic post is saved or any
 * comparison_category term is created/edited/deleted. This ensures the
 * frontend always shows fresh data after content changes, without waiting
 * for the 30-minute TTL to expire.
 *
 * Uses direct DB query to bulk-delete all transients matching the prefix,
 * which is more efficient than deleting them one by one.
 */
function cor_flush_block_cache(): void {
	global $wpdb;
	$wpdb->query( // phpcs:ignore WordPress.DB.DirectDatabaseQuery.DirectQuery,WordPress.DB.DirectDatabaseQuery.NoCaching
		"DELETE FROM {$wpdb->options}
		 WHERE option_name LIKE '_transient_cor_accordion_%'
		    OR option_name LIKE '_transient_timeout_cor_accordion_%'"
	);
}
add_action( 'save_post_comparison_topic', 'cor_flush_block_cache' );
add_action( 'edited_comparison_category', 'cor_flush_block_cache' );
add_action( 'created_comparison_category', 'cor_flush_block_cache' );
add_action( 'delete_comparison_category', 'cor_flush_block_cache' );

// On activation: register CPT + taxonomy early, then flush rewrite rules
// so that any REST API endpoints become immediately available.
register_activation_hook(
	__FILE__,
	function () {
		ComparisonTopic::register_post_type();
		ComparisonCategory::register_taxonomy();
		flush_rewrite_rules();
	}
);

// On deactivation: clean up rewrite rules.
register_deactivation_hook(
	__FILE__,
	function () {
		flush_rewrite_rules();
	}
);
